public class p03_LatinLetters {
    public static void main(String[] args) {
        for (char i = 'a'; i <= 'z'; i++) {
            System.out.println(i);
        }
        for (int i = 97; i <= 122; i++) {
            System.out.print((char)i);
        }
    }
}
